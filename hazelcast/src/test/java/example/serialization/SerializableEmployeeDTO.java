package example.serialization;

import java.io.Serializable;
import java.util.Objects;

public class SerializableEmployeeDTO implements Serializable {
    private final String name;
    private final int age;

    public SerializableEmployeeDTO(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) {
            return true;
        }
        if (o == null || getClass() != o.getClass()) {
            return false;
        }
        SerializableEmployeeDTO that = (SerializableEmployeeDTO) o;
        return age == that.age && Objects.equals(name, that.name);
    }

    @Override
    public int hashCode() {
        return Objects.hash(name, age);
    }
}
